package com.example.foodentertainmenet;

public class NewArrivalsItems {
    String ArrivalsName;
    int ArrivalsImage;

    public NewArrivalsItems(String arrivalsName, int arrivalsImage) {
        ArrivalsName = arrivalsName;
        ArrivalsImage = arrivalsImage;
    }

    public String getArrivalsName() {
        return ArrivalsName;
    }

    public int getArrivalsImage() {
        return ArrivalsImage;
    }

}
